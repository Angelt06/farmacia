import axios from "axios";

// Interfaz de prueba (Realmente por la api es de usuarios)
interface IPruebaMedicamento {
  name: string;
  phone: string;
  username: string;
  email: string;
  website: string;
}

const medicamentos = ref<IPruebaMedicamento[]>([]);

const consultarMedicamentos = async () => {
  const response = await axios.get<IPruebaMedicamento[]>(
    "https://jsonplaceholder.typicode.com/users"
  );

  response.data.forEach((medicamento) => {
    medicamentos.value.push({
      name: medicamento.name,
      phone: medicamento.phone,
      username: medicamento.username,
      email: medicamento.email,
      website: medicamento.website,
    });
  });
};

consultarMedicamentos();
